public class Contacto {
    private String Nombre;
    private int Numero;

    public Contacto() {
    }

    public Contacto(String Nombre, int Numero) {
        this.Nombre = Nombre;
        this.Numero = Numero;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public int getNumero() {
        return Numero;
    }

    public void setNumero(int Numero) {
        this.Numero = Numero;
    }

   
    
    
}
